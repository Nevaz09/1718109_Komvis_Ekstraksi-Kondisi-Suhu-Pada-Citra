% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
open=guidata(gcbo); 
a=get(open.axes1,'Userdata');%membuat var a yang mengambil nilai dari axes 1
c=rgb2hsv(a);%membuat variabel c yang berisi perubahan rgb ke hsv dari var a
 
 h=mean(mean(c(:,:,1)));% membuat variabel h yang mengambil rata-rata dari array ke-1 untuk nilai hue
 s=mean(mean(c(:,:,2)));% membuat variabel s yang mengambil rata-rata dari array ke-2 untuk nilai saturation
 v=mean(mean(c(:,:,3)));% membuat variabel v yang mengambil rata-rata dari array ke-3 untuk nilai value
 
 if (h < 0.28)
     {
         set(open.edit6,'string','hangat');%set mengisi nilai edit6 dengan 'hangat'
     }
 elseif ( h > 0.28) && (h < 0.58)
     {
         set(open.edit6,'string','normal');
     }   
 elseif ( h > 0.58)
     {
         set(open.edit6,'string','sedang');
     }
 end;
 
set(open.figure1,'CurrentAxes',open.axes2);
set(imagesc(c));
set(open.axes2,'Userdata',c);

 set(open.edit1,'string',h);%set mengisi nilai edit1 dengan nilai rata rata h
 set(open.edit2,'string',s);%set mengisi nilai edit1 dengan nilai rata rata s
 set(open.edit3,'string',v);%set mengisi nilai edit1 dengan nilai rata rata v

